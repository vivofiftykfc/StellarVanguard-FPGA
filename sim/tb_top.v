//===================================================================
// tb_top.v — STELLAR VANGUARD 仿真测试平台
//===================================================================
// 【功能】生成 100MHz 时钟，施加复位信号，测试以下功能：
//   - 状态转换（复位→标题→游戏中→死亡→游戏结束）
//   - 基本玩家左右移动
//   - 射击功能
//   - 内置 20 秒超时看门狗
//
// 运行命令: xvlog tb_top.v ../rtl/*.v; xelab tb_top; xsim tb_top
//===================================================================

`timescale 1ns / 1ps

module tb_top;

reg         CLK100MHZ;
reg         btnU, btnD, btnL, btnR, btnC;
wire [3:0]  VGA_R, VGA_G, VGA_B;
wire        VGA_HS, VGA_VS;
wire [6:0]  seg;
wire [3:0]  an;
wire [3:0]  led;

//———————————————————————————————————————————————————————————————————
// 例化待测设计 (DUT — Device Under Test)
//———————————————————————————————————————————————————————————————————
top u_dut (
    .CLK100MHZ (CLK100MHZ),
    .btnU      (btnU),
    .btnD      (btnD),
    .btnL      (btnL),
    .btnR      (btnR),
    .btnC      (btnC),
    .VGA_R     (VGA_R),
    .VGA_G     (VGA_G),
    .VGA_B     (VGA_B),
    .VGA_HS    (VGA_HS),
    .VGA_VS    (VGA_VS),
    .seg       (seg),
    .an        (an),
    .led       (led)
);

//———————————————————————————————————————————————————————————————————
// 100MHz 时钟发生器：周期 = 10ns，每5ns翻转一次
//———————————————————————————————————————————————————————————————————
initial CLK100MHZ = 1'b0;
always #5 CLK100MHZ = ~CLK100MHZ;

//———————————————————————————————————————————————————————————————————
// 测试激励序列
// 步骤：复位 → 检查标题状态 → 按开始键 → 检查游戏中状态 →
//       右移 → 射击10发子弹 → 左移 → 等待5秒观察 → 结束
//———————————————————————————————————————————————————————————————————
initial begin
    btnU = 1'b0; btnD = 1'b0; btnL = 1'b0;
    btnR = 1'b0; btnC = 1'b0;

    //—— 复位：先按下 btnD，等待100µs后释放 ——
    btnD = 1'b1;
    #100_000;       // 100µs
    btnD = 1'b0;
    #100_000;

    // 验证复位后是否进入 TITLE 标题状态（led[0] 应为1）
    if (led[0] !== 1'b1)
        $display("FAIL: 复位后应为 TITLE 状态, led=%b", led);
    else
        $display("PASS: 复位后成功进入 TITLE 状态");

    //—— 按下开始键 (btnC)，等待消抖后验证进入游戏中状态 ——
    btnC = 1'b1;
    #2_000_000;     // 按住2ms以通过消抖
    btnC = 1'b0;
    #2_000_000;

    if (led[1] !== 1'b1)
        $display("FAIL: 按 START 后应为 PLAYING 状态, led=%b", led);
    else
        $display("PASS: 按 START 后成功进入 PLAYING 状态");

    //—— 向右移动（btnR 按下5ms ≈ 300帧）——
    btnR = 1'b1;
    #5_000_000;     // 5ms (~300帧)
    btnR = 1'b0;
    #2_000_000;

    //—— 连续发射10发子弹 ——
    repeat (10) begin
        btnU = 1'b1;
        #2_000_000;
        btnU = 1'b0;
        #500_000;
    end

    //—— 向左移动 ——
    btnL = 1'b1;
    #5_000_000;
    btnL = 1'b0;
    #2_000_000;

    //—— 让游戏运行5秒，观察行为 ——
    #5_000_000_000;

    $display("仿真完成。得分可能已累积。");
    $display("检查波形: VGA_HS (~31.5kHz), VGA_VS (~60Hz)。");
    $finish;
end

//———————————————————————————————————————————————————————————————————
// 超时看门狗：如果仿真运行超过20秒则强制结束
//———————————————————————————————————————————————————————————————————
initial begin
    #20_000_000_000;
    $display("TIMEOUT after 20s");
    $finish;
end

endmodule
